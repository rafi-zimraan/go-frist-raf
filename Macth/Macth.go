package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println(math.Ceil(1.45))      // Membulatkan Keatas
	fmt.Println(math.Floor(1.45))     // Membulatkan Kebawah
	fmt.Println(math.Round(1.45))     // Membulatkan Terdekat
	fmt.Println(math.Max(20, 10))     // Maximum
	fmt.Println(math.Min(1.55, 2.55)) // Minimum
	fmt.Println(math.Sqrt(16))        // Akar Kuadrat
}
